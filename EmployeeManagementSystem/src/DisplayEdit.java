    public void setRefVal(EmployeeInfo refvalforEmployee) {
        refNum = refvalforEmployee;
        
        EmployeeNumber.setText(String.valueOf(refNum.empNum));
        FirstName.setText(refNum.firstName);
        LastName.setText(refNum.lastName);
        if (refNum.gender.equals("Male")) {
            MaleButton.setSelected(true);
        }
        if (refNum.gender.equals("Female")) {
            FemaleButton.setSelected(true);
        }
        WorkLocation.setText(refNum.workLoc);
        DeductRate.setText(String.valueOf(refNum.deductRate));
        
        if (refNum instanceof PTE) {
            PTE partTime = (PTE) refNum;

            PTEButton.setSelected(true);
            
            HourlyWage_YearlySalaryLabel.setText("Hourly Wage");
            HourlyWage_YearlySalary.setText(String.valueOf(partTime.hourlyWage));
            HourlyWage_YearlySalaryLabel.setVisible(true);
            HourlyWage_YearlySalary.setVisible(true);
            
            HoursPerWeekLabel.setText("Hours Per Week");
            HoursPerWeek.setText(String.valueOf(partTime.hoursPerWeek));
            HoursPerWeekLabel.setVisible(true);
            HoursPerWeek.setVisible(true);
            
            WeeksPerYearLabel.setText("Weeks Per Year");
            WeeksPerYear.setText(String.valueOf(partTime.weeksPerYear));
            WeeksPerYearLabel.setVisible(true);
            WeeksPerYear.setVisible(true);
        }
        else if (refNum instanceof FTE) {
            FTE fullTime = (FTE) refNum;
            
            FTEButton.setSelected(true);
            
            HourlyWage_YearlySalaryLabel.setText("Yearly Salary");
            HourlyWage_YearlySalary.setText(String.valueOf(fullTime.yearlySalary));
            HourlyWage_YearlySalaryLabel.setVisible(true);
            HourlyWage_YearlySalary.setVisible(true);
            
            HoursPerWeekLabel.setVisible(false);
            HoursPerWeek.setVisible(false);
            
            WeeksPerYearLabel.setVisible(false);
            WeeksPerYear.setVisible(false);
        }
    }
    
